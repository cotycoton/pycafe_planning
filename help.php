            <h2>Documentation</h2>
	    <p>Bonjour à toutes et à tous,</p>

Voici le nouveau planning permettant de mieux gérer le planning pour la tenue de l'épicafé. Il permet par rapport à la version précédente de faciliter votre inscription sur les différents créneaux (enfin j'espère), et d' éviter les bugs qui ont pu se produire sur certains créneaux. Les nouvelles inscriptions se font pour le mois d'avril.

		<h3>Connection</h3>
Pour vous connecter, rien de plus simple, c'est par ici, avec comme login l'adresse email que vous avez donné à l'épicafé, et comme mot de passe  epicafe!  Vous avez la possibilité de le changer si vous le souhaitez, mais aucune obligation !

		<div> 
                <img data-src="doc/planning_login.png" alt="Image 1" class="lazy-load">
		<figcaption>Page de connexion</figcaption>
		</div> 
		
		<h3>Navigation</h3>

Une seule semaine est affichée à la fois, <span class="jaune">surlignée en jaune </span> dans le mois auquel elle appartient. Pour passer d'une semaine à l'autre, vous pouvez soit cliquer sur la semaine du mois affiché, ou passer à la semaine précédente / suivante avec les flèches en haut de la page (entourée en rouge ci-dessous).
		
		<div> 
                <img data-src="doc/planning_nav.png" alt="Image 1" class="lazy-load">
		</div> 

		<h3>Inscription dans le planning</h3> 
		<p>
Pour s'inscrire sur <span class="highlight">oun créneau "ouvrable" (en bleu clair)</span> , il suffit de cliquer sur le créneau, un bouton ajouter apparaît <i class="bi bi-clipboard-plus"></i>. Tous les créneaux ne sont pas "réservables". Pour les événements ayant lieux en dehors des créneaux habituels, les créneaux correspondant passent également en bleu (Saint Patrick par exemple ci-dessus).
<br>Vous pouvez indiquer si vous souhaitez assurer le service avec une autre personne (une icône <i class="bi bi-people-fill"></i> apparait alors), et éventuellement s'associer à un événement s'il en existe un.</p>
 
		<h3> Suppresion du planning</h3>
Pour s'élever, il faut cliquer sur le créneau correspondant pour faire apparaître la poubelle <i class='bi bi-trash'></i>. La suppression nécessite alors une validation :  
		
		<div> 
                <img data-src="doc/planning_supression.png" alt="Image 1" class="lazy-load">
		<figcaption>Validation de la surpression</figcaption>
		</div> 
 
<b> Point important, vous ne pouvez que vous inscrire et vous supprimer . A l'épicafé, le téléphone permettant de gérer le terminal sumup permet d'inscrire ou d'enlever n'importe qui </b>.

<br>
Enfin, il est possible de cliquer sur un événement pour avoir plus de détails.
 

Voila, en espérant que ce mode de fonctionnement vous convienne. N'hésitez pas si vous avez des remarques ou si vous rencontrez des bugs.

<br>
Julien





